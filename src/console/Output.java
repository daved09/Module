package console;

public class Output {
	public static void out(String text){
		System.out.print(text);
	}
	public static void out(int number){
		System.out.print(number);
	}
	public static void out(double number){
		System.out.print(number);
	}
	public static void out(char character){
		System.out.print(character);
	}
	
	public static void outLine(String text){
		System.out.println(text);
	}
	public static void outLine(int number){
		System.out.println(number);
	}
	public static void outLine(double number){
		System.out.println(number);
	}
	public static void outLine(char character){
		System.out.println(character);
	}
}
